export type TranslationKey = keyof typeof translations.zh

let currentLocale: Locale = 'zh'

export function setLocale(locale: Locale) {
  currentLocale = locale
}

export function getLocale(): Locale {
  return currentLocale
}

export function translate(locale: Locale, key: string, params?: Record<string, string | number>) {
  const template = translations[locale][key as TranslationKey] ?? key

  if (!params) {
    return template
  }

  return template.replace(/\{(\w+)\}/g, (_, token: string) => String(params[token] ?? `{${token}}`))
}

export function t(key: string, params?: Record<string, string | number>) {
  return translate(currentLocale, key, params)
}
